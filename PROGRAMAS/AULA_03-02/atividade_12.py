#Utilizando a estrutura anterior, some dois valores se forem diferentes e multiplique-os se forem iguais.
n1 = int(input("Informe o primeiro número: "))
n2 = int(input("Informe o segundo número: ")) 
if n1 == n2:
 print("Os valores são iguais, então a multiplicação deles é : ", n1 * n2)
 #print("Seus Números são iguais !")
else : 
 print("Os valores são diferentges, então a soma deles é : " , n1 + n2)
